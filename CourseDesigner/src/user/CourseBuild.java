package user;

public class CourseBuild {
	
	private String courseName;
	
	private String buildTarget;
	
	private String buildPlanPath;

	public String getCourseName() {
		return courseName;
	}

	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}

	public String getBuildTarget() {
		return buildTarget;
	}

	public void setBuildTarget(String buildTarget) {
		this.buildTarget = buildTarget;
	}

	public String getBuildPlanPath() {
		return buildPlanPath;
	}

	public void setBuildPlanPath(String buildPlanPath) {
		this.buildPlanPath = buildPlanPath;
	}

	
	
	 

}
